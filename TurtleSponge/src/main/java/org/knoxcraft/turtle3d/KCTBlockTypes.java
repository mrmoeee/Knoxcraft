    /**
     * Create a Python class that looks like an enum that we can plug 
     * into pykc.py
     * 
     * @return
     */
    public static String makePykcEnum() {
        StringBuilder buf=new StringBuilder();
        buf.append("class BlockType:\n");
        buf.append("    '''Sort of like an enum, but we literally want the int values and we don't\n");
        buf.append("need any of the features we get from extending a Python Enum.\n");
        buf.append("'''\n");

        for (KCTBlockTypes t : KCTBlockTypes.values()){
            buf.append(String.format("    %s=\"%s\"\n", t.toString().toLowerCase(), t.toString()));
        }
        return buf.toString();
    }
    /**
     * Create a JavaScript function that creates pairings of blocks that we
     * can plug into the generators/javascript/turtle.js to use for Blockly.
     * 
     * @return
     */
    public static String makeBlocklyFunction() {
//function blockOptions() {
//    var options = [];
//    options.push(['air', '"AIR"']);
//    ...
//}
        StringBuilder buf=new StringBuilder();
        buf.append("/*\n");
        buf.append("*  Set block type with drop down.\n");
        buf.append("*  Generated by KCTBlockTypes.makeBlocklyFunction()\n");
        buf.append("*/\n");
        buf.append("function blockOptions() {\n");
        buf.append("    var options=[]\n");
        for (KCTBlockTypes t : KCTBlockTypes.values()){
            buf.append(String.format("    options.push(['%s', '\"%s\"']);\n", t.toString().toLowerCase(), t.toString()));
        }
        buf.append("}\n");
        return buf.toString();
    }
